//! Metadata and binary pattern scanning for tracking identifiers.
//!
//! Scans audio file tags (via lofty) and raw bytes for suspicious markers such as
//! AI-service watermarks, fingerprint identifiers, and tracking metadata.

use std::path::Path;

use lofty::prelude::*;
use serde::Serialize;

use crate::error::Result;

/// Metadata scan results
#[derive(Debug, Clone, Default, Serialize)]
pub struct MetadataScanResult {
    /// All tags found in the audio file.
    pub tags: Vec<TagInfo>,
    /// Binary patterns found that may indicate watermarks or tracking.
    pub suspicious_chunks: Vec<ChunkInfo>,
    /// General anomaly descriptions.
    pub anomalies: Vec<String>,
}

/// Information about a single metadata tag.
#[derive(Debug, Clone, Serialize)]
pub struct TagInfo {
    /// Tag format (e.g., "Id3v2", "VorbisComments").
    pub tag_type: String,
    /// Tag key or field name.
    pub key: String,
    /// Tag value content.
    pub value: String,
    /// Whether this tag is flagged as potentially tracking-related.
    pub suspicious: bool,
}

/// A suspicious binary pattern found in the file.
#[derive(Debug, Clone, Serialize)]
pub struct ChunkInfo {
    /// Human-readable description of the pattern.
    pub description: String,
    /// Byte offset where the pattern was found.
    pub offset: u64,
}

/// Scans audio files for metadata tags and suspicious binary patterns.
pub struct MetadataScanner;

impl MetadataScanner {
    /// Scan an audio file for metadata tags and suspicious binary patterns.
    pub fn scan(path: &Path) -> Result<MetadataScanResult> {
        let mut result = MetadataScanResult::default();

        if let Ok(tagged_file) = lofty::read_from_path(path) {
            for tag in tagged_file.tags() {
                let tag_type = format!("{:?}", tag.tag_type());
                for item in tag.items() {
                    let key = format!("{:?}", item.key());
                    let value = format!("{:?}", item.value());
                    let suspicious = is_suspicious_tag(&key, &value);
                    result.tags.push(TagInfo {
                        tag_type: tag_type.clone(),
                        key,
                        value,
                        suspicious,
                    });
                }
            }
        }

        // Binary scan for suspicious patterns
        if let Ok(data) = std::fs::read(path) {
            scan_binary_patterns(&data, &mut result);
        }

        Ok(result)
    }
}

fn is_suspicious_tag(key: &str, value: &str) -> bool {
    let key_lower = key.to_lowercase();
    let value_lower = value.to_lowercase();
    let suspicious_keys = [
        "unique",
        "fingerprint",
        "identifier",
        "tracking",
        "license",
        "isrc",
        "barcode",
        "upc",
        "ean",
        "catalog",
        "txxx",
    ];
    let suspicious_values = [
        "suno",
        "udio",
        "audiocraft",
        "musicgen",
        "stable audio",
        "ai-generated",
        "generated by",
    ];
    suspicious_keys.iter().any(|k| key_lower.contains(k))
        || suspicious_values.iter().any(|v| value_lower.contains(v))
}

fn scan_binary_patterns(data: &[u8], result: &mut MetadataScanResult) {
    let patterns: &[(&[u8], &str)] = &[
        (b"SUNO", "AI watermark marker"),
        (b"UDIO", "Udio AI marker"),
        (b"AudioCraft", "Meta AudioCraft marker"),
        (b"MusicGen", "MusicGen marker"),
        (b"Stable Audio", "Stable Audio marker"),
        (b"APETAGEX", "APE tag"),
        (b"ID3", "ID3 tag header"),
    ];

    for (pattern, description) in patterns {
        for (offset, window) in data.windows(pattern.len()).enumerate() {
            if window == *pattern {
                result.suspicious_chunks.push(ChunkInfo {
                    description: description.to_string(),
                    offset: offset as u64,
                });
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_suspicious_tag_keys() {
        assert!(is_suspicious_tag("UniqueFileID", "some-value"));
        assert!(is_suspicious_tag("TXXX:Fingerprint", "abc123"));
        assert!(is_suspicious_tag("ISRC", "US1234567890"));
        assert!(is_suspicious_tag("Tracking-ID", "xyz"));
    }

    #[test]
    fn test_suspicious_tag_values() {
        assert!(is_suspicious_tag("Comment", "Generated by Suno AI"));
        assert!(is_suspicious_tag("Encoder", "Udio v2"));
        assert!(is_suspicious_tag("Software", "AudioCraft toolkit"));
        assert!(is_suspicious_tag("Tool", "MusicGen model"));
    }

    #[test]
    fn test_non_suspicious_tags() {
        assert!(!is_suspicious_tag("Title", "My Song"));
        assert!(!is_suspicious_tag("Artist", "John Doe"));
        assert!(!is_suspicious_tag("Album", "Greatest Hits"));
        assert!(!is_suspicious_tag("Genre", "Rock"));
    }

    #[test]
    fn test_scan_binary_patterns_finds_markers() {
        let mut data = vec![0u8; 100];
        data[10..14].copy_from_slice(b"SUNO");
        data[50..54].copy_from_slice(b"UDIO");

        let mut result = MetadataScanResult::default();
        scan_binary_patterns(&data, &mut result);
        assert_eq!(result.suspicious_chunks.len(), 2);
        assert_eq!(result.suspicious_chunks[0].offset, 10);
        assert_eq!(result.suspicious_chunks[1].offset, 50);
    }

    #[test]
    fn test_scan_binary_patterns_clean_data() {
        let data = vec![0u8; 100];
        let mut result = MetadataScanResult::default();
        scan_binary_patterns(&data, &mut result);
        assert!(result.suspicious_chunks.is_empty());
    }

    #[test]
    fn test_scan_binary_id3_header() {
        let mut data = vec![0u8; 50];
        data[0..3].copy_from_slice(b"ID3");
        let mut result = MetadataScanResult::default();
        scan_binary_patterns(&data, &mut result);
        assert_eq!(result.suspicious_chunks.len(), 1);
        assert!(result.suspicious_chunks[0].description.contains("ID3"));
    }
}
